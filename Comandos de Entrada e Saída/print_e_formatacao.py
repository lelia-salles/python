# Exemplo de uso de diferentes caracteres de escape no print

# Tabulação horizontal
print("Coluna1\tColuna2\tColuna3")

# Tabulação vertical (pode não ser visível em alguns terminais)
print("Linha1\vLinha2\vLinha3")

# Aspas duplas
print("Aqui está uma aspa dupla: \"")

# Aspas simples
print('Aqui está uma aspa simples: \'')

# Beep de alerta (pode não ser audível em alguns sistemas)
print("Beep de alerta: \a")

# Caractere nulo (não visível na saída)
print("Caractere nulo no meio: \0")

# Quebra de linha
print("Linha 1\nLinha 2\nLinha 3")

# Combinação de vários caracteres
print("Tabulação horizontal: Col1\tCol2\tCol3\nAspas simples: \'\nAspas duplas: \"\nBeep: \a\nNulo: \0\nQuebra de linha: Linha1\nLinha2")
